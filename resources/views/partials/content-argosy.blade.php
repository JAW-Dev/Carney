    <div class="wp-block-carney-capability-preview" id="audience" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Audience</small>
                Defining your audience
            </h3>
            <p class="lead">Understanding exactly who your audience is will help create content they will connect with and want to share. This phase can't be guess work. We always research the personas of our client’s audience — current, former, and potential students — before creating any content.</p>
            <p class="lead">This research goes far beyond the demographics of your audience. You'll want to get into the psychographics — the attitudes, interests, opinions, and lifestyles — of your audience.</p>
            <p class="lead">This crucial step will help you create content that speaks to the busy working mother who wants to go back to school, and content that speaks to the recent high-school grad looking for the perfect learning environment for college.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="style" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
