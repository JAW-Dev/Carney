                <small class="mb-2">Frequency</small>
                Establishing a publishing frequency
            </h3>
            <p class="lead">Frequency is one of the most important pieces in your playbook. This will help with content planning and ensuring that every piece gets edited in a timely manner. A consistent frequency will also keep your audience engaged because they’ll know when to expect the latest blog post.</p>
            <p class="lead">Since engaging blog content takes time to create, edit, and publish, we’ll lean on Argosy’s expertise and data to determine the ideal day, or days, of the week to publish content. We want to make sure that content gets maximum reach. A schedule is a way to ensure this.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="distribution" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Distribution</small>
                Getting in front of the right people
            </h3>
            <p class="lead">You’ve likely heard the phrase, “content is king.” That is true, but distribution is queen, and she runs the castle. Without a strong strategy for distribution of your content, no one will find it and read it.</p>
            <p class="lead">All blog posts should be optimized for SEO, ensuring that Google and other search engines crawl and rank it. This will improve your organic traffic.</p>
            <p class="lead">Social media should also be a central piece to distribution. The content Argosy publishes must be engaging and easily shareable. This will help you reach a new audience, who may not have otherwise considered attending Argosy.</p>
            <p class="lead">Email marketing is the third piece of distribution. Blog content and email marketing go together like students and books. New blog posts should be included in the latest emails. This gives your current audience a chance to share your content with their audiences.</p>
            <p class="lead">The last piece of distribution is influencer marketing. You can use influencers in a few ways. First, invite popular influencers to write guest posts for your blog. Those influencers will share the content with their audiences, who may be new and current students. On the other side, when you create blog content, you can work with influencers to amplify it.</p>
            <p class="lead">This strategy ensures that current and potential students will see your content, no matter where they spend their time on the internet.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="analytics" data-scroll-duration="4000">
