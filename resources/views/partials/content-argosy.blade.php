                <small class="mb-2">Objectives</small>
                Why you need a blog
            </h3>
            <p class="lead">In 2018, most brands have an established digital publication — a blog. Creating a blog is about more than just "keeping up with the Joneses." Blogs are a way to reach new audiences, stay in touch with your current customers, and restart conversations with old customers.</p>
            <p class="lead">But, creating a blog that accomplishes these goals means building a clearly defined blogging strategy. Think of this as your blog's playbook. This playbook outlines who your audience is, how you're going to talk to them, where you're going to reach them, and how you can define success.</p>
