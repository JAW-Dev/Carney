        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Style</small>
                Building the blog's "voice"
            </h3>
            <p class="lead">Brand voice humanizes your blog. The right voice will make readers feel like a close friend is talking to them, rather than a salesman trying to sell furniture.</p>
            <p class="lead">The blog’s voice will mirror Art Van’s brand voice. You’ll want it to convey enthusiasm, confidence, and authenticity, with a touch of cleverness. At the same time, the voice will steer clear of "fluff" words and unnecessary sales jargon.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="content" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Content</small>
                Determining the content
            </h3>
            <p class="lead">A smart blog playbook involves more than just writing blog posts. You need to create content that engages your audience in many different ways.</p>
            <p class="lead">For example, if you write a blog post about the latest styles for bedrooms, you would want to pair that with social media posts, videos that show the latest bedroom sets, custom images, and more.</p>
            <p class="lead">Before creating any content, you should always start with keyword research. Keyword research gives you the ability to know what your audience is interested in, rather than blindly guessing.</p>
            <p class="lead">Conducting keyword research involves enough steps that we could write a whole book on it, but here’s the 10,000 foot overview:</p>
            <ul class="lead">
              <li>Start by analyzing site-searches from your Google Analytics. This tells you the keywords that people are searching for on your website only. Then, compile topic ideas based around popular keywords.</li>
              <li>Next, use a tool like Keyword Planner, by Google, to determine the organic search frequency and ranking difficulty of each idea. You should be looking for terms that are high frequency and low ranking difficulty.</li>
              <li>Use a combination of KWFinder and AnswerThePublic to get a better understanding of the long-tail keywords people are searching for. These long-tail keywords will give you additional topic ideas to write about.</li>
            </ul>
            <p class="lead">Based on that research, the next step is to define a few central themes for your blog. Central themes reinforce the types of content you publish. Once search engines understand that, they’re more likely to rank you highly, which will drive more website traffic.</p>
            <p class="lead">From there, it’s on to writing. Blogs should be written in a way that engages past, current, and future customers. Some blogs can do all three. For example, if you a blog about a living room makeover could appeal to everyone. On the other side, a blog about a new product line, could be written in a way to connect with past customers.</p>
            <p class="lead">Blog posts can also be written to different audiences within past, current, and future customers. You could write a blog about furniture choices for your first apartment. That topic would appeal to a different audience than a blog about a dining room makeover, which would likely appeal to an older audience.</p>
            <p class="lead">But, that’s not all. You can also write gender-specific blogs. A post about the perfect man-cave would likely appeal to men. Compare that to a post about a home office, which could be written with women in mind.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="frequency" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Frequency</small>
                Establishing a publishing frequency
            </h3>
            <p class="lead">Frequency is one of the most important pieces in your playbook. This will help with content planning and ensuring that every piece gets edited in a timely manner. A consistent frequency will also keep your audience engaged because they’ll know when to expect the latest blog post.</p>
            <p class="lead">Since engaging blog content takes time to create, edit, and publish, Art Van should post a new blog every Wednesday. This will give each new post time to build up social media engagement before the weekend. That will help ensure that people are thinking “Art Van” when they’re doing their weekend shopping.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="distribution" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Distribution</small>
                Getting in front of the right people
            </h3>
            <p class="lead">You’ve likely heard the phrase, “content is king.” That is true, but distribution is queen, and she runs the castle. Without a strong strategy for distribution of your content, no one will find it and read it.</p>
            <p class="lead">All blog posts should be optimized for SEO, ensuring that Google and other search engines crawl and rank it. This will improve your organic traffic, which will help you capture new customers.</p>
            <p class="lead">Social media will also be a central piece to distribution. The content Art Van publishes must be engaging and easily shareable. This will help Art Van reach a new audience, who may not have otherwise considered buying furniture from their local store.</p>
            <p class="lead">Email marketing is the third piece of distribution. Blog content and email marketing go together like a new leather armchair and matching ottoman. New blog posts should be included in the latest emails. This gives your current audience a chance to share your content with their audiences.</p>
            <p class="lead">The last piece of distribution is influencer marketing. You can use influencers in a few ways. First, invite popular influencers to write guest posts for your blog. Those influencers will share the content with their audiences, who may be new and past customers. On the other side, when you create blog content, you can work with influencers to amplify it.</p>
            <p class="lead">This strategy ensures that current, former, and potential customers will see your content, no matter where they spend their time on the internet.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="analytics" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Analytics</small>
                Defining success
            </h3>
            <p class="lead">Content is an investment so you need to make sure that you’re getting a return on that investment. It’s important to define Key-Performance Indicators before creating your new blog strategy.</p>
            <p class="lead">Successful content creates engagement in the form of social shares, drives website visitors from social media and search engines, and increases e-commerce conversions. To determine success, Art Van should track these metrics:</p>
            <ul class="lead">
              <li>Visits to the blog from social media</li>
              <li>Visits to the blog from search engines</li>
              <li>Percentage of new visits versus repeat visits</li>
              <li>Average pages per visit</li>
              <li>Percentage of online customers who visited the blog</li>
            </ul>
