                <small class="mb-2">Objectives</small>
                Why you need a blog
            </h3>
            <p class="lead">In 2018, most brands have an established digital publication — a blog. Creating a blog is about more than just "keeping up with the Joneses." Blogs are a way to reach new audiences, stay in touch with your current customers, and restart conversations with old customers.</p>
            <p class="lead">But, creating a blog that accomplishes these goals means building a clearly defined blogging strategy. Think of this as your blog's playbook. This playbook outlines who your audience is, how you're going to talk to them, where you're going to reach them, and how you can define success.</p>
        </div>
    </div>

    <div class="wp-block-carney-capability-preview" id="audience" data-scroll-duration="4000">
        <div class="wp-block-carney-capability-preview__content">
            <h3 class="wp-block-carney-capability-preview__title h5 mb-3">
                <small class="mb-2">Audience</small>
                Defining your audience
            </h3>
            <p class="lead">Understanding exactly who your audience is will help create content they will connect with and want to share. This phase can't be guess work. You need to research the personas of your audience — current, former, and potential customers — before creating any content.</p>
            <p class="lead">This research goes far beyond the demographics of your audience. You'll want to get into the psychographics — the attitudes, interests, opinions, and lifestyles — of your audience.</p>
            <p class="lead">This crucial step will help you create content that the busy working mother will take time out of her day to read, and content that speaks to the recent college grad looking for the perfect furniture for his first apartment.</p>
        </div>
    </div>

